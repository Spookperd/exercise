/**
 * Returns workout object for given week (1-12) and rotation index
 * rotationIndex: 0=fullbody, 1=upper, 2=lower
 */
export function getWorkoutForWeek(week, rotationIndex) {
  let block
  if (week <= 4) block = 1
  else if (week <= 8) block = 2
  else block = 3

  const typeMap = ['fullbody', 'upper', 'lower']
  const type = typeMap[rotationIndex % 3]
  const workoutId = `b${block}_${type}`
  return WORKOUTS.find((w) => w.id === workoutId) || WORKOUTS[0]
}

// ============================================================
// Helper: buildSetQueue
// ============================================================

/**
 * Builds a flat set queue from a workout ID and active substitutions map.
 * Each queue item: { exerciseId, setNumber, totalSets, restAfter, isSuperset, supersetPartner, supersetRole }
 *
 * For supersets: A1 set then A2 set interleaved per round.
 * restAfter for A1 items = 0, for A2 items = exercise.restSeconds
 */
export function buildSetQueue(workoutId, substitutions = {}) {
  const workout = WORKOUTS.find((w) => w.id === workoutId)
  if (!workout) return []

  const exerciseIds = workout.exercises

  // Group exercises by superset group
  // Build a map: groupKey -> [exId_order1, exId_order2]
  const supersetGroups = {}
  const nonSupersetIds = []

  for (const exId of exerciseIds) {
    const ex = EXERCISES[exId]
    if (!ex) continue
    if (ex.supersetGroup) {
      const key = exId.match(/b\d/)?.[0]
        ? `${ex.supersetGroup}_${exId.split('-').pop()}`
        : ex.supersetGroup

      // Use workout position to group correctly
      const groupKey = `${workoutId}_${ex.supersetGroup}`
      if (!supersetGroups[groupKey]) {
        supersetGroups[groupKey] = []
      }
      supersetGroups[groupKey].push(exId)
    } else {
      nonSupersetIds.push(exId)
    }
  }

  // Sort superset group members by supersetOrder
  for (const key of Object.keys(supersetGroups)) {
    supersetGroups[key].sort((a, b) => {
      return (EXERCISES[a]?.supersetOrder || 0) - (EXERCISES[b]?.supersetOrder || 0)
    })
  }

  // Build queue items in workout exercise order
  const queue = []
  // Track which superset groups we've already processed
  const processedGroups = new Set()

  for (const exId of exerciseIds) {
    const ex = EXERCISES[exId]
    if (!ex) continue

    if (ex.supersetGroup) {
      const groupKey = `${workoutId}_${ex.supersetGroup}`
      if (processedGroups.has(groupKey)) continue
      processedGroups.add(groupKey)

      const members = supersetGroups[groupKey] || []
      if (members.length < 2) {
        // Treat as normal if partner not found
        const totalSets = ex.workingSets
        for (let s = 1; s <= totalSets; s++) {
          queue.push({
            exerciseId: exId,
            setNumber: s,
            totalSets,
            restAfter: ex.restSeconds,
            isSuperset: false,
            supersetPartner: null,
            supersetRole: null,
          })
        }
        continue
      }

      const [ex1Id, ex2Id] = members
      const ex1 = EXERCISES[ex1Id]
      const ex2 = EXERCISES[ex2Id]
      const totalSets = Math.max(ex1?.workingSets || 1, ex2?.workingSets || 1)

      // Interleave: A1-set1, A2-set1, A1-set2, A2-set2, ...
      for (let s = 1; s <= totalSets; s++) {
        if (s <= (ex1?.workingSets || 0)) {
          queue.push({
            exerciseId: ex1Id,
            setNumber: s,
            totalSets: ex1?.workingSets || 1,
            restAfter: 0,
            isSuperset: true,
            supersetPartner: ex2Id,
            supersetRole: 'A1',
          })
        }
        if (s <= (ex2?.workingSets || 0)) {
          queue.push({
            exerciseId: ex2Id,
            setNumber: s,
            totalSets: ex2?.workingSets || 1,
            restAfter: ex2?.restSeconds || 90,
            isSuperset: true,
            supersetPartner: ex1Id,
            supersetRole: 'A2',
          })
        }
      }
    } else {
      const totalSets = ex.workingSets
      for (let s = 1; s <= totalSets; s++) {
        queue.push({
          exerciseId: exId,
          setNumber: s,
          totalSets,
          restAfter: ex.restSeconds,
          isSuperset: false,
          supersetPartner: null,
          supersetRole: null,
        })
      }
    }
  }

  return queue
}
